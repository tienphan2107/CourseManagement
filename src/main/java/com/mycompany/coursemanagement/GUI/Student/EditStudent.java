    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //Xác nhận
        int choose;
        Person rootStudent = personBUS.getStudentByID(Integer.parseInt(txtStudentID.getText()));
        Person changedStudent = new Person();
        changedStudent.setPersonID(Integer.parseInt(txtStudentID.getText()));
        if (checkString(txtFirstName.getText()) == true && checkString(txtLastName.getText()) == true) {
            changedStudent.setFirstName(txtFirstName.getText());
            changedStudent.setLastName(txtLastName.getText());
        } else {
            JOptionPane.showMessageDialog(this, "The changed infomation is invalid !");
            return;
        }
        Date temp = dtpickerEnrollmentDate.getDate();
        java.sql.Date enrollmentDate = new java.sql.Date(temp.getTime());
        changedStudent.setEnrollmentDate(enrollmentDate);
        if (rootStudent.getFirstName().equals(changedStudent.getFirstName()) && rootStudent.getLastName().equals(changedStudent.getLastName())) {
            this.dispose();
        } else {
            choose = JOptionPane.showConfirmDialog(this, "Edit This Student ?", "Confirm", JOptionPane.YES_NO_OPTION);
            if (choose == JOptionPane.NO_OPTION) { // đổi ý không xóa nữa
                return;
            } else {
                try {
                    boolean result = personBUS.EditStudent(changedStudent);
                    if (result == true) {
                        JOptionPane.showMessageDialog(this, "Edit Success !");
                        this.dispose();
                    }
                } catch (Exception ex) {
                    Logger.getLogger(EditStudent.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
        try {
            Person rootStudent = personBUS.getStudentByID(Integer.parseInt(txtStudentID.getText()));
            Person changedStudent = new Person();
            changedStudent.setPersonID(Integer.parseInt(txtStudentID.getText()));
            changedStudent.setFirstName(txtFirstName.getText());
            changedStudent.setLastName(txtLastName.getText());
            Date temp = dtpickerEnrollmentDate.getDate();
            java.sql.Date enrollmentDate = new java.sql.Date(temp.getTime());
            changedStudent.setEnrollmentDate(enrollmentDate);
            if (rootStudent.getFirstName().equals(changedStudent.getFirstName()) && rootStudent.getLastName().equals(changedStudent.getLastName())) {
                this.dispose();
            } else {
                int choose = JOptionPane.showConfirmDialog(this, "Cancel Edit This Student ?\n The edited won't be saved !", "Confirm", JOptionPane.YES_NO_OPTION);
                if (choose == JOptionPane.NO_OPTION) { // đổi ý không xóa nữa
                    return;
                } else {
                    this.dispose();
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        } 
    }//GEN-LAST:event_btnExitActionPerformed
    private boolean checkString(String input) {
        if (input.isEmpty()) {
            return false;
        }
        // Cho phép chữ cái từ A-Z, a-z và các ký tự tiếng Việt (bao gồm các dấu)
        return input.matches("[\\p{L} ]+");
    }
