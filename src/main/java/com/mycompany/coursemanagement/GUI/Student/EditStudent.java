    public void LoadValueComponent(int studentID) {
        try {
            Person editableStudent = personBUS.getStudentByID(studentID);
            if (editableStudent != null) {
                txtStudentID.setText(Integer.toString(editableStudent.getPersonID()));
                txtFirstName.setText(editableStudent.getFirstName());
                txtLastName.setText(editableStudent.getLastName());
                Date date = editableStudent.getEnrollmentDate();
                dtpickerEnrollmentDate.setDate(date);
                dtpickerEnrollmentDate.setEnabled(false);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "An error occured , please try again.", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
            return;
        }

    }

